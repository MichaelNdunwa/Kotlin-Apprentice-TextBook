/**
 * Given the following code:
 *      age = 16
 *      print(age)
 *      age = 30
 *      print(age)
 *  Declare age so that it compiles. Did you use var or val?
 */

fun main() {
    var age =16
    print(age)
    age = 30
    print(age)
}